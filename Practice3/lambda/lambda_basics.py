# Example 1: One parameter
f1 = lambda x: x + 10

# Example 2: Two parameters
f2 = lambda a, b: a * b

# Example 3: String transformation
f3 = lambda s: s.upper()

# Example 4: Check even
f4 = lambda n: n % 2 == 0

# Example 5: Power
f5 = lambda x: x ** 2


print(f1(5))
print(f2(2, 3))
print(f3("hello"))
print(f4(10))
print(f5(4))