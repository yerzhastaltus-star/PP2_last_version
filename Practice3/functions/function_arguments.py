# Example 1: One argument
def f1(a):
    print(a)


# Example 2: Two arguments
def f2(a, b):
    print(a + b)


# Example 3: Multiple arguments
def f3(name, age):
    print(name, age)


# Example 4: Three arguments
def f4(x, y, z):
    print(x * y * z)


# Example 5: String argument
def f5(text):
    print(text)


f1(5)
f2(2, 3)
f3("Ann", 18)
f4(2, 3, 4)
f5("Hello")