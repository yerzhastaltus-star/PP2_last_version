# Example 1: Return a constant
def r1():
    return 5


# Example 2: Return computed value
def r2(x):
    return x * 2


# Example 3: Return sum
def r3(a, b):
    return a + b


# Example 4: Return boolean
def r4(n):
    return n % 2 == 0


# Example 5: Return power
def r5(x):
    return x ** 2


print(r1())
print(r2(3))
print(r3(4, 5))
print(r4(10))
print(r5(6))